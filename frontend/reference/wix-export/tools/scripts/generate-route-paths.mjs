import { mkdir, readFile, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const rootDir = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const appFile = path.join(rootDir, 'src/App.jsx');
const outputFile = path.join(rootDir, 'src/generated/routePaths.js');

function cleanRoutePath(pathname) {
  if (!pathname || pathname === '/' || pathname === '/index.html') {
    return '/';
  }

  return pathname.endsWith('.html') ? pathname.slice(0, -5) || '/' : pathname;
}

const source = await readFile(appFile, 'utf8');
const paths = new Set(['/']);

for (const match of source.matchAll(/<Route\s+path="([^"]+)"/g)) {
  if (match[1] !== '*') {
    paths.add(cleanRoutePath(`/${match[1]}`));
  }
}

const routePaths = [...paths].sort((a, b) => a.localeCompare(b));
const output = `// Generated by scripts/generate-route-paths.mjs. Do not edit by hand.\n` +
  `export const routePaths = ${JSON.stringify(routePaths, null, 2)};\n`;

await mkdir(path.dirname(outputFile), { recursive: true });
await writeFile(outputFile, output);
console.log(`Generated ${routePaths.length} route paths.`);
